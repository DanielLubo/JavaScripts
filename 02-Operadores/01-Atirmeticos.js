// Operadores - Aritmeticos

let a = 5;
let b = 8;

console.log(a + b, 'Adicion'); // Adicion
console.log(a - b, 'Resta'); // Resta
console.log(a * b, 'Multiplicacion'); // Multiplicacion
console.log(a / b, 'Division'); // Division
console.log(a % b, 'Modulo'); // Modulo
console.log(a ** b, 'Potencia'); // Potencia

// Incrementar
console.log(++a); // De esta forma Primero Incrementa el valor de 'a' y lo muestra en la consola
console.log(a++); // De esta forma Primero muestra el valor de 'a' y luego incrementa su valor
console.log(a);

// Decrementar
console.log(--a);   
console.log(a--);
console.log(a);